public class DemoInrementDecrement {

    public static void main(string [] args) {

        //Nilai Angka
        system.out.println("Nilai x : " + x);
        system.out.println("Nilai y = " + y);
        system.out.println("Nilai z = " + z);

        //Increment
        x++; y++;
        system.out.println("Nilai x : " + x);
        system.out.println("Nilai y = " + y++);
        system.out.println("Nilai z = " + ++z);
        system.out.println("Nilai y : " + y);

        //Decrement
        x--; y--;
        system.out.println("Nilai x : " + x);
        system.out.println("Nilai y = " + y--);
        system.out.println("Nilai z = " + --z);
        system.out.println("Nilai y : " + y);
    }
}